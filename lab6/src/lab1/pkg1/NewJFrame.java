    private void DownloadTextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DownloadTextActionPerformed
         JFileChooser chooser = new JFileChooser();
    if (chooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {

        int choice = JOptionPane.showConfirmDialog(
                this,
                "Очистить исходную коллекцию?",
                "Подтверждение",
                JOptionPane.YES_NO_OPTION
        );

        if (choice == JOptionPane.YES_OPTION) {
            arrInteg.clear();
            ((DefaultTableModel) jTable1.getModel()).setRowCount(0);
        }

        try (BufferedReader br = new BufferedReader(new FileReader(chooser.getSelectedFile()))) {

            DefaultTableModel tModel = (DefaultTableModel) jTable1.getModel();

            String line;
            while ((line = br.readLine()) != null) {
                String[] p = line.split(" ");
                double low = Double.parseDouble(p[0]);
                double up = Double.parseDouble(p[1]);
                double step = Double.parseDouble(p[2]);
                double res = Double.parseDouble(p[3]);

                RecIntegral rec = new RecIntegral(low, up, step, res);
                arrInteg.add(rec);

                tModel.addRow(new Object[]{low, up, step, res});
            }

            JOptionPane.showMessageDialog(this, "Файл успешно загружен!");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Ошибка загрузки: " + ex.getMessage());
        }
    }
    }//GEN-LAST:event_DownloadTextActionPerformed

    private void DownloadBinaryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DownloadBinaryActionPerformed
     JFileChooser chooser = new JFileChooser();
    if (chooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {

        int choice = JOptionPane.showConfirmDialog(
                this,
                "Очистить исходную коллекцию?",
                "Подтверждение",
                JOptionPane.YES_NO_OPTION
        );

        if (choice == JOptionPane.YES_OPTION) {
            arrInteg.clear();
            ((DefaultTableModel) jTable1.getModel()).setRowCount(0);
        }

        try (ObjectInputStream in = new ObjectInputStream(
                new FileInputStream(chooser.getSelectedFile()))) {

            ArrayList<RecIntegral> loaded = (ArrayList<RecIntegral>) in.readObject();

            DefaultTableModel tModel = (DefaultTableModel) jTable1.getModel();

            for (RecIntegral rec : loaded) {
                arrInteg.add(rec);
                tModel.addRow(new Object[]{
                        rec.getLowLim(),
                        rec.getUpLim(),
                        rec.getStep(),
                        rec.geResult()
                });
            }

            JOptionPane.showMessageDialog(this, "Данные загружены (двоичный формат)");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Ошибка загрузки: " + ex.getMessage());
        }
    }
    }//GEN-LAST:event_DownloadBinaryActionPerformed

    
