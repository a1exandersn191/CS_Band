  private void startServer() {
    // Поток для HELLO
    new Thread(() -> {
        try {
            serverSocket = new DatagramSocket(5000);
            byte[] buf = new byte[1024];
            DatagramPacket packet = new DatagramPacket(buf, buf.length);

            while (true) {
                serverSocket.receive(packet);
                String msg = new String(packet.getData(), 0, packet.getLength());

                if (msg.equals("HELLO")) {
                    InetSocketAddress addr = new InetSocketAddress(packet.getAddress(), packet.getPort());
                    clients.add(addr);

                    SwingUtilities.invokeLater(() ->
                        JOptionPane.showMessageDialog(this, "Клиент подключён: " + addr)
                    );
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }).start();

    // Поток для результатов
    new Thread(() -> {
        try {
            resultSocket = new DatagramSocket(5001);

            while (true) {
                byte[] buf = new byte[1024];
                DatagramPacket p = new DatagramPacket(buf, buf.length);
                resultSocket.receive(p);

                String msg = new String(p.getData(), 0, p.getLength());
                String[] parts = msg.split(" ");

                double value = Double.parseDouble(parts[1]);

                synchronized (this) {
                    partialResults.add(value);

                    if (partialResults.size() == expectedParts) {
                        double total = partialResults.stream().mapToDouble(Double::doubleValue).sum();
                        partialResults.clear();

                        SwingUtilities.invokeLater(() -> {
                            DefaultTableModel t = (DefaultTableModel) jTable1.getModel();
                            t.setValueAt(total, currentRow, 3);
                        });
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }).start();
}
