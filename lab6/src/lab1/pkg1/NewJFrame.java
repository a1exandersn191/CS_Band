    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
                                      
    DefaultTableModel tModel = (DefaultTableModel) jTable1.getModel();
    int rowNum = jTable1.getSelectedRow();

    if (rowNum == -1) {
        JOptionPane.showMessageDialog(null, "Выберите строку!");
        return;
    }

    double low = Double.parseDouble(tModel.getValueAt(rowNum, 0).toString());
    double up = Double.parseDouble(tModel.getValueAt(rowNum, 1).toString());
    double step = Double.parseDouble(tModel.getValueAt(rowNum, 2).toString());

    for (int i = arrInteg.size() - 1; i >= 0; i--) {
        RecIntegral r = arrInteg.get(i);
        if (r.getLowLim() == low &&
            r.getUpLim() == up &&
            r.getStep() == step) 
        {
            arrInteg.remove(i);
            break;
        }
    }

    tModel.removeRow(rowNum);
    }//GEN-LAST:event_jButtonDeleteActionPerformed

